package com.aokyu.pocket.http;

/**
 * The available methods for {@link HttpClient}.
 */
public enum HttpMethod {

    /**
     * The HTTP GET method.
     */
    GET,

    /**
     * The HTTP POST method.
     */
    POST,

    /**
     * The HTTP PUT method.
     */
    PUT,

    /**
     * The HTTP DELETE method.
     */
    DELETE;
}
